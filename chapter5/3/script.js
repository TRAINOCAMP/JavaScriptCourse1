// 関数の引数にnumber1とnumber2を追加してください
const sum = (number1, number2) => {
  //関数の中にnumber1とnumber2を合計した値をコンソールに出力する処理を書きます。
  console.log(number1+number2);
};

//関数sumの呼び出して実行します。
//  第一引数は10, 第二引数は5を渡します。
sum(10,5); // 15
