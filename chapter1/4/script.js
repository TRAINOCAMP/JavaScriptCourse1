//変数 number はすでに宣言されて定義されています。
let number = 10;
console.log(number); // 10

// 変数 numberの値に 11を足してください。足し算は基本形で書いてください。
number = number + 11;//
// 変数 numberの値をコンソール画面に出力してください。
console.log(number);

// 変数 numberの値を 3で割ってください。割り算は省略形で書いてください。
number /= 3;

// 変数 numberの値をコンソール画面に出力してください。
console.log(number); // 7

