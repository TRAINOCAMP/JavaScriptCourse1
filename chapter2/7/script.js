//定数colorが定義されています。初期値は数値の"青"です。
const color = "青";
//定数colorに対する switch文が途中まで作成されています。
switch(color){
    case "青": 
    console.log("進んでください！");
    break;

    case "黄":
    console.log("気を付けてください！");
    break;

    case "赤": console.log("止まってください！");
    break;

}

//switch文の作成ができたら、colorの定数を赤や青や黄に変えてみて、
//正しく条件分岐ができているか確認してみましょう。
