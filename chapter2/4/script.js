//定数ageが定義されています。初期値は数値の18です。
const age = 18;

//ageの値が20以上かという条件のif文を作成してください。
//if文の中で"私の年齢は20歳以上です。"とコンソール画面に出力してください。
//ifの条件が不成立の場合は "私の年齢は20歳未満です。"とコンソール画面に出力してください。
if (age>=20){
    console.log("私の年齢は20歳以上です。");
}else{
    console.log("私の年齢は20歳未満です。");
}

// if文の作成ができたら、ageの定数の初期値を"22"など別の数値にしてみて、正しく条件分岐ができているか確認してみてください。
